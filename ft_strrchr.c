char	*ft_strrchr(const char *s, int c)
{
	char	a;
	int		b;

	a = (char)c;
	b = 0;
	while (s[b] != '\0')
		b++;
	while (b > 0)
	{
		if (s[b] == a)
			return ((char *)s + b);
		b--;
	}
	if (s[b] == a)
		return ((char *)s + b);
	return (0);
}
